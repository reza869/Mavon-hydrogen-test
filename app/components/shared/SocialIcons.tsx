const ICON_COMPONENTS: Record<
  SocialLink['platform'],
  ({className}: {className: string}) => JSX.Element
> = {
  twitter: TwitterIcon,
  facebook: FacebookIcon,
  pinterest: PinterestIcon,
  instagram: InstagramIcon,
  tiktok: TikTokIcon,
};

export function SocialIcons({
  links = DEFAULT_SOCIAL_LINKS,
  size = 'md',
  className = '',
  gap = 'md',
}: SocialIconsProps) {
  const iconSize = ICON_SIZES[size];
  const gapSize = GAP_SIZES[gap];

  return (
    <div className={`flex items-center ${gapSize} ${className}`}>
      {links.map((link) => {
        const IconComponent = ICON_COMPONENTS[link.platform];
        return (
          <a
            key={link.platform}
            href={link.url}
            aria-label={link.label}
            className="text-foreground hover:opacity-70 transition-opacity duration-200"
            target="_blank"
            rel="noopener noreferrer"
          >
            <IconComponent className={iconSize} />
          </a>
        );
      })}
    </div>
  );
}
